    private void resetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetActionPerformed
n = Integer.parseInt(size.getText());
board =  new int[n][n];
bx= Screen.getWidth();
by = Screen.getHeight();
cellx = bx/n;
celly = by/n;
Screen.setSize(cellx*n, celly*n);
drawpanel.repaint();
    }//GEN-LAST:event_resetActionPerformed

