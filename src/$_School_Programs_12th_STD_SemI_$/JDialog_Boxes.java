    private void DisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DisplayActionPerformed
int option =  -1;
int msgType=-1;
        

if(Plain.isSelected())
msgType=-1;
else if(Error.isSelected())
msgType=0;    
else if(Information.isSelected())
msgType=1;
else if(Warning.isSelected())
msgType=2;
else if(Question.isSelected())
msgType=3;

if(Ok.isSelected())
option=-1;
else if(YesNo.isSelected())
option=0;    
else if(YesNoCancel.isSelected())
option=1;
else if(OkCancel.isSelected())
option=2;

if(MessageJD.isSelected()==true)
JOptionPane.showMessageDialog(rootPane,JDMessage.getText(),JDTitle.getText(), msgType);
else if(InputJD.isSelected()==true)
JOptionPane.showInputDialog(rootPane,JDMessage.getText(),JDTitle.getText(), msgType);
else if(ConfirmationJD.isSelected()==true)
JOptionPane.showConfirmDialog(rootPane,JDMessage.getText(),JDTitle.getText(),option,msgType);
else if(OptionJD.isSelected()==true)
JOptionPane.showOptionDialog(rootPane, JDMessage.getText(), JDTitle.getText(), option, msgType, null, null,"");




/*
Message :(  , int messageType, Icon icon)
Confirmation :(  , int optionType, int messageType, Icon icon)
Input :(  , int messageType, Icon icon, Object[] selectionValues, Object initialSelectionValue)
Option :(  , int optionType, int messageType, Icon icon, Object[] options, Object initialValue)
*/        
    }//GEN-LAST:event_DisplayActionPerformed

    private void ConfirmationJDItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_ConfirmationJDItemStateChanged
if(ConfirmationJD.isSelected()==true){
Ok.setEnabled(true);
YesNo.setEnabled(true);
YesNoCancel.setEnabled(true);
OkCancel.setEnabled(true);
}
else{
Ok.setEnabled(false);
YesNo.setEnabled(false);
YesNoCancel.setEnabled(false);
OkCancel.setEnabled(false);
}
    }//GEN-LAST:event_ConfirmationJDItemStateChanged

