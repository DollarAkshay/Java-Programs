ResultSet Querry(String query)
{
ResultSet rs = null;
try{
Class.forName("java.sql.DriverManager");
Connection con = 
DriverManager.getConnection("jdbc:mysql://localhost:3306/EMP","root", "student");
Statement stmt = (Statement) con.createStatement();
rs = stmt.executeQuery(query);
}
catch(Exception e)
{
JOptionPane.showMessageDialog (this, e.getMessage());
}
return rs;
}

