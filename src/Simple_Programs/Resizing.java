private void imgMouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imgMouseMoved
if((getMousePosition().x>=img.getX())&&(getMousePosition().x<=img.getX()+10))
    img.setCursor(new Cursor(Cursor.E_RESIZE_CURSOR));
else if((getMousePosition().x>=img.getX()+img.getWidth()-3)&&(getMousePosition().x<=img.getX()+img.getWidth()+7))
    img.setCursor(new Cursor(Cursor.W_RESIZE_CURSOR));
else
img.setCursor(new Cursor(Cursor.DEFAULT_CURSOR));


        

    
    
    // TODO add your handling code here:
}//GEN-LAST:event_imgMouseMoved

private void imgMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imgMouseDragged
n1.setText(""+img.getCursor().toString());
if(img.getCursor().toString().equals("java.awt.Cursor[East Resize Cursor]"))
{
setCursor(new Cursor(Cursor.E_RESIZE_CURSOR));   
img.setSize(640-getMousePosition().x+36+7,img.getHeight());
img.setLocation(getMousePosition().x-7,img.getY());
}

}//GEN-LAST:event_imgMouseDragged

