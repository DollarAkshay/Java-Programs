    private void IncrementActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IncrementActionPerformed
String temp = str.getText();
start = Integer.parseInt(range_start.getText());
end = Integer.parseInt(range_end.getText());
i=start;
while(i<=end){
String s=temp;
s=s.replaceAll("&",i+"");

if(coma.isSelected())
ta.append(s+"");
else
ta.append(s+",");
++i;
}
ta.append("\n");
    }//GEN-LAST:event_IncrementActionPerformed

