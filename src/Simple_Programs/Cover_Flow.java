  public final class InnerClassDrawingPanel extends javax.swing.JPanel {
      @Override
      protected void paintComponent(Graphics g) {
Graphics2D g2d = (Graphics2D)g;
super.paintComponent(g2d);
g2d.setBackground(Color.BLACK);
g2d.clearRect(0, 0, Screen.getWidth(), Screen.getHeight());


try {
llimg = ImageIO.read(new File(getClass().getResource("/other_Program_Images/Album_"+(no-2)+".png").toURI()));
limg = ImageIO.read(new File(getClass().getResource("/other_Program_Images/Album_"+(no-1)+".png").toURI()));
cimg = ImageIO.read(new File(getClass().getResource("/other_Program_Images/Album_"+(no)+".png").toURI()));
rimg = ImageIO.read(new File(getClass().getResource("/other_Program_Images/Album_"+(no+1)+".png").toURI()));
rrimg = ImageIO.read(new File(getClass().getResource("/other_Program_Images/Album_"+(no+2)+".png").toURI()));
}         
catch (URISyntaxException ex) {
Logger.getLogger(Cover_Flow.class.getName()).log(Level.SEVERE, null, ex);
} 
catch (IOException e) {}

g2d.translate(Screen.getWidth()/2,Screen.getHeight()/2);
double ss=(double)(800+Math.abs(dx)*4/5)/1000;
double sc=(double)(1250-Math.abs(dx)*1.8)/1000;
int px=(int)(-cimg.getWidth()/2 +dx),
py=-cimg.getHeight()/2;

//Left & Right Image
g2d.scale(ss,ss);
g2d.drawImage(limg,null,px-250,py);
g2d.drawImage(rimg,null,px+250,py);
//Centre Image
g2d.scale(sc,sc);
g2d.drawImage(cimg,null,px,py);
g2d.shear(0,1-ss/1);
g2d.drawImage(llimg,null,px-500,py);
g2d.drawImage(rrimg,null,px+500,py);



        drawpanel.setAutoscrolls(true);
          }}
