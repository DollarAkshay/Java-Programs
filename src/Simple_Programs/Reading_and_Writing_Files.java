    private void fcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fcActionPerformed
Charset charset = Charset.forName("US-ASCII");
        
if((fc.APPROVE_OPTION==0)&&(fc.getDialogType()==JFileChooser.OPEN_DIALOG)){
try{
ta.setText("");
BufferedReader reader = new BufferedReader(new FileReader(fc.getSelectedFile()),32768);
String line;
while ((line = reader.readLine()) != null) {
ta.append(line+"\n");
}
chooser.setVisible(false);
}
catch (Exception e) {}
}

else if((fc.APPROVE_OPTION==0)&&(fc.getDialogType()==JFileChooser.SAVE_DIALOG)){
try {
BufferedWriter writer = new BufferedWriter(new FileWriter(fc.getSelectedFile()),32768);
writer.write(ta.getText());
writer.close();
chooser.setVisible(false);
}
catch (IOException ex) {
JOptionPane.showMessageDialog(null, ex.getMessage());
}
}



    }//GEN-LAST:event_fcActionPerformed

