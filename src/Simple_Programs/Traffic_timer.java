void signal()
{
if(light<60)
    Lights.setIcon(new ImageIcon(getClass().getResource("/other_Program_Images/red_light.gif")));
else if((light>=60)&&(light<=63))
        Lights.setIcon(new ImageIcon(getClass().getResource("/other_Program_Images/yellow_light.gif")));
else if(light>63)
        Lights.setIcon(new ImageIcon(getClass().getResource("/other_Program_Images/green_light.gif")));
light++;
if(light==100)
    light=0;


}        
    
