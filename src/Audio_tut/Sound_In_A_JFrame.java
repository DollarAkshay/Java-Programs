    private void playActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playActionPerformed
    try {
         // Open an audio input stream.
         URL url = this.getClass().getClassLoader().getResource("Ring.wav");
         t.setText(url+"");
         AudioInputStream audioIn = AudioSystem.getAudioInputStream(url);
         // Get a sound clip resource.
         Clip clip = AudioSystem.getClip();
         // Open audio clip and load samples from the audio input stream.
         clip.open(audioIn);
         clip.start();
      } catch (UnsupportedAudioFileException e) {
         e.printStackTrace();
      } catch (IOException e) {
         e.printStackTrace();
      } catch (LineUnavailableException e) {
         e.printStackTrace();
      }        // TODO add your handling code here:
    }//GEN-LAST:event_playActionPerformed

    private void Ringtone_ChooserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Ringtone_ChooserActionPerformed
if(Ringtone_Chooser.APPROVE_OPTION==0){
    try {
         // Open an audio input stream.
         url = Ringtone_Chooser.getSelectedFile().toURL();
         t.setText(url+""+i++);
         AudioInputStream audioIn = AudioSystem.getAudioInputStream(url);
         // Get a sound clip resource.
         Clip clip = AudioSystem.getClip();
         // Open audio clip and load samples from the audio input stream.
         clip.open(audioIn);
         clip.loop(0);
      } catch (UnsupportedAudioFileException e) {
         e.printStackTrace();
      } catch (IOException e) {
         e.printStackTrace();
      } catch (LineUnavailableException e) {
         e.printStackTrace();
      }  
}
    }//GEN-LAST:event_Ringtone_ChooserActionPerformed

